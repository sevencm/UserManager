package com.sevencm.user;
/**
 * 
 */
public class UserInfo {
	/**
	 * 
	 */
	String strUserID; 
	String strUserName;
	String strUserPassword;
	String strUserSex;	
	String strUserMail;
	String strUserAddress;
	int intUserAge;
	
	public String getStrUserID() {
		return strUserID;
	}
	public void setStrUserID(String strUserID) {
		this.strUserID = strUserID;
	}
	public String getStrUserName() {
		return strUserName;
	}
	public void setStrUserName(String strUserName) {
		this.strUserName = strUserName;
	}
	public String getStrUserPassword() {
		return strUserPassword;
	}
	public void setStrUserPassword(String strUserPassword) {
		this.strUserPassword = strUserPassword;
	}
	public String getStrUserSex() {
		return strUserSex;
	}
	public void setStrUserSex(String strUserSex) {
		this.strUserSex = strUserSex;
	}
	public String getStrUserMail() {
		return strUserMail;
	}
	public void setStrUserMail(String strUserMail) {
		this.strUserMail = strUserMail;
	}
	public String getStrUserAddress() {
		return strUserAddress;
	}
	public void setStrUserAddress(String strUserAddress) {
		this.strUserAddress = strUserAddress;
	}
	public int getIntUserAge() {
		return intUserAge;
	}
	public void setIntUserAge(int intUserAge) {
		this.intUserAge = intUserAge;
	}
	
		
}
